num1 = 10
num2 = 5
// Addition
sum_result = num1 + num2
console.log(sum_result);

//  Subtraction
difference = num1 - num2
console.log(difference);


// Multiplication
product = num1 * num2
console.log(product);


// Division
quotient = num1 / num2
console.log(quotient);


// Modulus (Remainder)
remainder = num1 % num2
console.log(remainder);
