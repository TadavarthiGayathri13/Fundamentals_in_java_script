// Logical AND (&&): if both operands  are true it returns true otherwise false
let x = true;
let y = false;
console.log(x && y); 
console.log(x && true); 

// Logical OR (||): any one is true it gives true otherwise false
let a = true;
let b = false;
console.log(a || b);
console.log(false || b); 

// Logical NOT (!): operator takes truth to falsity and vice versa
let p = true;
let q = false;
console.log(!p); 
console.log(!q); 




